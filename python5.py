dia = int(input("Dame un número: "))

if dia == 1 :
    print("Lunes")
elif dia == 2 :
    print("Martes")
elif dia == 3 :
    print("Miércoles")
elif dia == 4 :
    print("Jueves")
elif dia == 5 :
    print("Viernes")
elif dia == 6 :
    print("Sábado")
elif dia == 7 :
    print("Domingo")
else :
    print("Fuera rango")